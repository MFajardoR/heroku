package com.example.mongodb.model;

/**
 *
 * @author marco
 */
public class Persona {
    
    int id;
    String cedula;
    String nombre;
    String contacto;
    int edad;

    public Persona(int id, String cedula, String nombre, String contacto, int edad) {
        this.id = id;
        this.cedula = cedula;
        this.nombre = nombre;
        this.contacto = contacto;
        this.edad = edad;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getContacto() {
        return contacto;
    }

    public void setContacto(String contacto) {
        this.contacto = contacto;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
}
